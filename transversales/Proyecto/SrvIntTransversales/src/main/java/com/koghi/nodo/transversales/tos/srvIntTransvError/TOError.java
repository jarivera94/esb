package com.koghi.nodo.transversales.tos.srvIntTransvError;

public class TOError {
	private String id;
	private String idError;
	private String valCodigo;
	private String valDescripcion;
	private String valExcepcion;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIdError() {
		return idError;
	}
	public void setIdError(String idError) {
		this.idError = idError;
	}
	public String getValCodigo() {
		return valCodigo;
	}
	public void setValCodigo(String valCodigo) {
		this.valCodigo = valCodigo;
	}
	public String getValDescripcion() {
		return valDescripcion;
	}
	public void setValDescripcion(String valDescripcion) {
		this.valDescripcion = valDescripcion;
	}
	public String getValExcepcion() {
		return valExcepcion;
	}
	public void setValExcepcion(String valExcepcion) {
		this.valExcepcion = valExcepcion;
	}
}